'use strict';

describe('Controller Tests', function() {

    describe('Course Management Detail Controller', function() {
        var $scope, $rootScope;
        var MockEntity, MockPreviousState, MockCourse, MockOfficeHour, MockStudent, MockProject, MockInstructor;
        var createController;

        beforeEach(inject(function($injector) {
            $rootScope = $injector.get('$rootScope');
            $scope = $rootScope.$new();
            MockEntity = jasmine.createSpy('MockEntity');
            MockPreviousState = jasmine.createSpy('MockPreviousState');
            MockCourse = jasmine.createSpy('MockCourse');
            MockOfficeHour = jasmine.createSpy('MockOfficeHour');
            MockStudent = jasmine.createSpy('MockStudent');
            MockProject = jasmine.createSpy('MockProject');
            MockInstructor = jasmine.createSpy('MockInstructor');
            

            var locals = {
                '$scope': $scope,
                '$rootScope': $rootScope,
                'entity': MockEntity,
                'previousState': MockPreviousState,
                'Course': MockCourse,
                'OfficeHour': MockOfficeHour,
                'Student': MockStudent,
                'Project': MockProject,
                'Instructor': MockInstructor
            };
            createController = function() {
                $injector.get('$controller')("CourseDetailController", locals);
            };
        }));


        describe('Root Scope Listening', function() {
            it('Unregisters root scope listener upon scope destruction', function() {
                var eventType = 'a2App:courseUpdate';

                createController();
                expect($rootScope.$$listenerCount[eventType]).toEqual(1);

                $scope.$destroy();
                expect($rootScope.$$listenerCount[eventType]).toBeUndefined();
            });
        });
    });

});
